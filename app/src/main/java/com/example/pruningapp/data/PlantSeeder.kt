package com.example.pruningapp.data

import com.google.gson.Gson

object PlantSeeder {

    private val gson = Gson()

    private data class SeedPlant(
        val name: String,
        val botanicalName: String,
        val type: String,
        val rules: List<Triple<String, String, String>>,
        val instructions: List<String> = emptyList(),
        val harvestStart: String? = null,
        val harvestEnd: String? = null
    )

    suspend fun seed(db: AppDatabase) {
        ALL_PLANTS.forEach { sp ->
            val id = db.plantDao().insertPlant(
                Plant(
                    name = sp.name,
                    botanicalName = sp.botanicalName,
                    type = sp.type,
                    instructions = gson.toJson(sp.instructions),
                    harvestStart = sp.harvestStart,
                    harvestEnd = sp.harvestEnd
                )
            )
            sp.rules.forEach { (ruleType, start, end) ->
                db.plantDao().insertPruningRule(
                    PruningRule(plantId = id, startMonthDay = start, endMonthDay = end, type = ruleType)
                )
                db.generateTasksForRule(id, start, end, ruleType)
            }
        }
    }

